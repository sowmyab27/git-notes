CREATE TABLE ODC
(
ODC_ID INT PRIMARY KEY,
ODC_NAME VARCHAR(50)NOT NULL
)
CREATE TABLE IT_EMPLOYEES
(
EMP_ID INT PRIMARY KEY,
EMP_NAME VARCHAR (50) NOT NULL,
GENDER CHAR(6),
SALARY MONEY,
DOJ DATE,
ODC_ID INT
FOREIGN KEY (ODC_ID) REFERENCES ODC(ODC_ID)
)
