SELECT 
COUNT(*) AS TOTAL_IT_EMP,
SUM(SALARY) AS TOTAL_SALARY,
AVG(SALARY) AS AVG_SALARY,
MIN(SALARY) AS MIN_SALARY,
MAX(SALARY) AS MAX_SALARY
FROM IT_EMPLOYEES

SELECT
D.ODC_NAME,
COUNT(*)  AS TOTAL_STAFF,
SUM(E.SALARY) AS TOTALAMOUNT,
MAX(E.SALARY) AS MAXAMOUNT,
AVG(E.SALARY) AS AVGTOTAL
FROM IT_EMPLOYEES AS E
INNER JOIN ODC AS D
ON E.ODC_ID=D.ODC_ID
GROUP BY D.ODC_NAME
HAVING COUNT(*)>1
ORDER BY AVGTOTAL DESC;

